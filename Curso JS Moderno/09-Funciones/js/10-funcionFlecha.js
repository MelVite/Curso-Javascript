/**Arrow Functions o funciones flecha =>
 * Son una forma más concisa de escribir funciones en JS
 * Proporcionan una sintaxis más breve y clara.
 * Se define usando la flecha => en lugar de la palabra function
 *  */ 

const funcion = function() {
    console.log('Aprendiendo Javascript');
}

//Función flecha
const funcionFlecha = ( ) => {
    console.log('Aprendiendo Javascript');

}

funcionFlecha();

//Forma más corta para función de una línea
const funcionFlecha2 = () => 'Aprendiendo a programar en Javascript'

console.log(funcionFlecha2());