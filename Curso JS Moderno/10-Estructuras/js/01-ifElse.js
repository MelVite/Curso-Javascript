//Creando un if, se trata de una estructura de control
const puntaje = 1001;

if(puntaje == 1000) { //Entre parentesis se pone la condicion que se debe cumplir
    console.log('El puntaje es igual a 1000');//entre {} se coloca lo que debe pasar si se cumple la condición
} else { //en caso de no cumplir la condición
    console.log('El puntaje es diferente a 1000');
}
