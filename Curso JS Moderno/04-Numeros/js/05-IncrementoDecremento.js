
let puntaje = 5;

//Incremento 
puntaje++; //imprime primero el valor original y al volver a llamar ya le hace el incremento, imprime 5

++puntaje; //imprime directamente el valor con el incremento, imprime 6

let puntaje2 = 7;

//Decremento
puntaje2--; //imprime 7

--puntaje2; //imprime 6

//Valores mayores a 1 
puntaje += 3;
puntaje2 -= 6;

console.log(puntaje);
console.log(puntaje2);